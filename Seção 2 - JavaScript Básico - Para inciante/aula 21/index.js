/**
 * Operadores de comparação
 * > Maior que
 * >= Maior que ou Igual
 * < Menor
 * <= Menor que ou Igual
 * == Igualdade (valor)
 * === Igualdade Estrito (valor e tipo)
 * != Dideferente (valor)
 * !== Diferente Estrito (valor e tipo)
 */

// Maior que
const n1 = 10;
const n2 = 5;
console.log(`${n1} > ${n2}, Maior que:`, n1 > n2);
console.log(`${n1} >= ${n2}, Maior que ou igual:`, n1 >= n2);
console.log(`${n1} < ${n2}, Menor que:`, n1 < n2);
console.log(`${n1} <= ${n2}, Menor que ou igual:`, n1 <= n2);
console.log(`${n1} == ${n2}, Igualdade (valor):`, n1 == n2);
console.log(`${n1} === ${n2}, Igualdade Expressiva (valor e tipo):`, n1 === n2);
console.log(`${n1} != ${n2}, Diferente (valor):`, n1 != n2);
console.log(`${n1} !== ${n2}, Diferente Expressiva (valor e tipo)`, n1 !== n2);
